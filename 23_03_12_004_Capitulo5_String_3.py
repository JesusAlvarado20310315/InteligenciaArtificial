"""En este capitulo se manejaron los tipos de acciones que puede realizar las variables de tipo texto."""

"""En el capitulo se vieron 3 instrucciones diferentes por lo cual vamos a generar 3 variables diferentes tipo String para mostrar
lo que hace cada una de las instrucciones vistas"""
var1 = "enrique barros fernandez"
var2 = "Esta Es Una Frase Para Ser Formateada"
var3 = "Esta Es Una Frase Para Ser Formateada"

"""Las instrucciones vistas fueron 'title()' la cual hace que la primera letra de todas las palabras dentro de un String se
transformen a mayusculas y las demas a minusculas, la siguiente fue 'lower()' la cual tiene como objetivo convertir toda la cadena
en letras minusculas mientras el caso de 'upper()' tiene la funcion inversa de convertir toda la cadena en letras mayusculas"""
print(var1.title())
print(var2.lower())
print(var3.upper())