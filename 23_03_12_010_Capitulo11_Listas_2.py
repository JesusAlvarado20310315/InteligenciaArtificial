"""Para continuar con las listas tenemos que hablar acerca de las listas con numeros negativos estos numeros lo que hacen es
contar la lista desde la parte final por lo cual se tiene que declara como si fuera negativo (-1)"""

"""En este ejemplo se nos pide que obtengamos los String de 'naranja', 'amarillo', 'lila', 'blanco', 'rojo'"""
col = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
print(col[-1])
print(col[-7])
print(col[-5])
print(col[-2])
print(col[-10])
"""Estos valores los tenemos que calcular con los numeros negativos en las listas por lo cual tenemos que contar desde el ultimo
por lo cual el registro negativo para el String 'naranja' es el '-1', para el String 'amarillo' es el '-7', para el String 'lila'
es el '-5', para el String 'blanco' es el '-2' y para el String 'rojo' es el '-10'"""