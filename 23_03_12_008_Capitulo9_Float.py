"""El capitulo aborda el tema de 'floats' los cuales son variables numericas con punto decimal lo cual puede significar que el 
numero sea infinito pero para ayudar con esto existe el metodo 'round' el cual hace que dependiendo de la cantidad de decimales
que se le establesca dejara estos decimales en lugar de imprimir el numero completo0"""
num = round(17.567383292929200234, 5)
"""En este caso tenemos un numero ya fijo con 18 numeros depues del punto decimal pero con ayude del metodo 'round' lo dejameros
solo en 5 numeros despues del punto"""
print(num)