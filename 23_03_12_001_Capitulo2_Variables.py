"""Para crear variables es tan sencillo como poner como quieres que esta se llame e igualarla a lo que sea que necesites ya sea una
cadena de caracteres o un numero"""
respuesta = "El resultado de la suma es:"
"""En la linea anterior podemos ver el ejemplo de varaible llamada 'respuesta' la cual esta igualada a la cadena de caracteres
'El resultado de la suma es:' """
var1 = 5
var2 = 3
"""En las 2 lineas de codigo anteriores se declaran 2 variables 'var1' y 'va2' las cuales estan igualadas a los valores numericos
'5' y '3' respectivamnete"""
"""Cabe resaltar que si quieres declarar una variable con una cadena de caracteres es necesario guardarla dentro de comillas,
mientras que si lo que necesitas es un valor numerio este solo se guarda sin la necesidad de las comillas"""
res = var1 + var2
"""En la linea anterior tenemos la variable llamada 'res' esta variable esta igualada con las 2 variables anteriores las cuales
son 'var1' y 'var2' y tambien sumas los valores que estas tiene"""
print(respuesta,res)
"""Aqui podemos ver el resultado impreso con el comando print y adentro las 2 variables tanto con 'respuesta' como 'res'
al estar ya declaradas las variables solo se tienen que poner con una coma para que ambas se puedan visualizar"""